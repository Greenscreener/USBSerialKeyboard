    public static char translateChar(char inputCharacter, int layoutIndex) {
        for (int i=0; i < layoutsArray[layoutIndex].length; i++) {
            if (layoutsArray[layoutIndex][i][0].charAt(0) == inputCharacter) {
                return layoutsArray[layoutIndex][i][1].charAt(0);
            }
        }
        switch (inputCharacter) {
            case ' ':
                return ' ';
            case '\n':
                return '\n';
            default:
                return Character.MIN_VALUE;
        }
    }
